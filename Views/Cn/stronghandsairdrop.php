                                <strong>All xRhodium holders can participate</strong> and their wallet addresses will be included automatically. Thus, <strong>no specific actions need to be taken</strong> by the users to enroll in the programme cycles. An <strong>automatic snapshot</strong> of the individual balances will take place at the beginning and the end of every 3-month cycle (note that the closing snapshot for one cycle is also the opening snapshot for another cycle). <strong>Rewards will be then automatically transferred</strong> to the addresses that managed to avoid any negative balance movement.
